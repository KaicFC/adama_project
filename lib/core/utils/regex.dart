  static String cpf = r"^[0-9]{3}.?[0-9]{3}.?[0-9]{3}-?[0-9]{2}";
  static String phone =
      r'^\(?\+?[0-9]{1,3}\)? ?-?[0-9]{1,3} ?-?[0-9]{3,5} ?-?[0-9]{4}( ?-?[0-9]{3})? ?(\w{1,10}\s?\d{1,6})?';
}
